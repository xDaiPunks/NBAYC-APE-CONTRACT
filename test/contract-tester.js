async function mintBatch() {
  const totalSupply = await contract.methods.MAX_SUPPLY().call();
  const batchSize = 20;
  const batchPrice = BigInt(batchSize) * BigInt(8e18);
  console.log("Batch price: ", batchPrice.toString());

  for (let i = 0; i < totalSupply / batchSize; i++) {
    const tx = contract.methods.mint(batchSize);
    const gasEstimate = await tx.estimateGas({
      from: account.address,
      value: batchPrice.toString(),
    });
    const gas = Math.floor(gasEstimate * 2);
    const gasPrice = await web3.eth.getGasPrice();
    const data = tx.encodeABI();
    const nonce = await web3.eth.getTransactionCount(account.address);

    console.log("txdata:", tx, gas, gasPrice, data, nonce);

    const signedTx = await web3.eth.accounts.signTransaction(
      {
        to: contractAddress,
        data,
        gas,
        gasPrice,
        nonce,
        value: batchPrice.toString(),
      },
      privateKey
    );

    await web3.eth.sendSignedTransaction(signedTx.rawTransaction);
  }
}

mintBatch().then(() => console.log("Minting complete!"));
